from math import sin, cos, tan, radians
anguloqualquer = float(input('digite um angulo: '))
seno = sin(radians(anguloqualquer))
print(f'o angulo qualquer {anguloqualquer} tem o seno {seno} ')
cosseno = cos(radians(anguloqualquer))
print(f'o angulo qualquer {anguloqualquer} tem o cosseno {cosseno}')
tangente = tan(radians(anguloqualquer))
print(f'o anglo quaquer {anguloqualquer} tem a tangente {tangente}')
